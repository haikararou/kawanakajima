			<h3 class="c-heading__m">三.海津城築城のこと</h3>
		</div>
		<div class="c-section__column2__w65">
			<h4 class="c-heading__s">信玄公、馬場・山本に命じ、川中島に城を築かせる</h4>
			<p>　『土塊鑑（つちがくれかがみ）』※によると埴科郡<a href="https://kawanakajima.nagano.jp/tour/tour-212/">海津の城</a>は天文二十二年（1553）築城とある。 <a href="https://kawanakajima.nagano.jp/tour/tour-176/">雨の宮</a>の合戦は雪に難儀し、空しく引き取ったことから、<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は、何とかして信州川中島に城を築き、領国支配の拠点にしたいと痛切に思われていた。そこで、場所見立てを<a href="https://kawanakajima.nagano.jp/character/baba-nobufusa">馬場美濃守</a>と<a href="https://kawanakajima.nagano.jp/character/yamamoto-kansuke">山本勘助</a>に仰せつけた。両人はあちこち調査したところ、埴科郡松井郷小渕の里が要害まことに優れたところと見極め、城を築くことにした。</p>
            <p>　小渕の里には、清野道寿軒、息子の左衛門尉（清寿軒）が建てた要害堅固の城館跡があった。東・西・南は高山が屏風となって連なり、北は<a href="https://kawanakajima.nagano.jp/tour/tour-327/">善光寺</a>まで平原だが、千曲川、犀川の大河で防戦上実に優れている。上手と下手はいずれも千曲川が押し迫り、道は細く、自然の関所となって敵を防ぎ止めることは容易である。これこそ青龍・白虎・玄武・朱雀の四神［しじん］（天の四方の方角を司る守護神）が相応じている。</p>
            <p>　<a href="https://kawanakajima.nagano.jp/character/yamamoto-kansuke">山本勘助</a>は、角隅右宗より授かった古法に基づき四神を四方に祀り、地鎮祭を執り行った。その後、四方に堀を極め、築城の段取りを終え、人夫を集めて急ぎ築城したのが、<a href="https://kawanakajima.nagano.jp/tour/tour-212/">海津の城</a>である。 ※松代を中心に川中島四郡の地理・伝説・寺社縁起等を収録した宝永3年（1706）の本。松代藩士・落合保孝編著。</p>

			<h4 class="c-heading__s">勘助、城北に水除八幡の宮を建立す</h4>
			<p>　<a href="https://kawanakajima.nagano.jp/tour/tour-212/">海津城</a>の築城にあたり、 <a href="https://kawanakajima.nagano.jp/character/yamamoto-kansuke">山本勘助</a>は考えた。北面の防護堀と利用したこの城は、後々千曲川が満水すれば、水難を避けがたい。その上、今年は丑年。丑の方角は北、水である。十干（じっかん）も癸（みずのと）なれば極陰である。こうしたことから、水を守りの神として祀ることにした。そこで、城の北、川中島に一社を建立して武大神を祀り、水除八幡の宮とした。この社が今の八幡原にある水除八幡宮である。</p>

			<h4 class="c-heading__s">海津の名の由来</h4>
			<p>　この城が海津と名がついた説はいろいろある。海なしの信濃国に「海」の字を用いるのを不思議がる者もいることだろう。高井・水内・更級・埴科・小県の五郡は、太古は海であった。この五郡の井戸水は塩辛く、塩分が含まれ茶の湯によい。深い土中からは貝の類が多く出土する。地名には海野（東御市海野）、塩尻（上田市塩尻）、塩崎（長野市篠ノ井）の名があり、石川（長野市篠ノ井）には昔船をつないだと伝わる船繋（ふなつな）ぎ石という岩もある。矢代（千曲市屋代）や上田辺りでは、野良仕事で田畑に出ることを「沖へ行く」という。水内郡も海水の内という意味である。</p>
            <p>　このようなことから海津と名づけたのである。また、築城に際し、甲州石水の図を採用して築いた城ということで「甲斐図」ともいうのである。</p>
            <p>　こうして、<a href="https://kawanakajima.nagano.jp/tour/tour-212/">海津城</a>城代として本丸に小山田備中守、<a href="https://kawanakajima.nagano.jp/tour/tour-166/">尼飾城</a>には<a href="https://kawanakajima.nagano.jp/character/kousaka-danjo">高坂弾正</a>を差し置いた。海津に城が築かれると、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>の出陣に備えて、この地から甲州まで連絡の狼煙（のろし）を定めた。これは西条山（狼煙山）に上がる火を、葛尾山（埴科郡坂城町）、腰越山中久保（小県郡長和町長久保）、和田峠（長和町～下諏訪町）、金沢（茅野市金沢）、若神子（山梨県北杜市須玉町）を経由して、石水の御館（甲府躑躅ヶ館［躑躅ヶ崎館］）まで伝えるという工夫である。常に足軽二十人ずつを置いて、山を守らせた。海津より石水※までは二十七里の距離であった。</p>
            <p>※不詳。石水寺山城のことか？（信玄が生まれた積翠寺要害城）躑躅ヶ崎館の北方にある。</p>
