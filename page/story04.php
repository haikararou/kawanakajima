            <h4 class="c-heading__s">信玄公と謙信公、決着のため川中島へ出馬す</h4>
			<p>　武田軍・上杉軍は、呉越の隔たりのように、和睦することなく、たびたび出陣しては徒（いたず）らに対陣を重ねていた。今度こそは勝負を決着しようと、<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は、将兵を引き連れ川中島へ出馬した。両者とも日本の名将。その徳によって天下の英雄たちは、この両将の旗下に馳せ参じた。</p>
            <p>　時に天文二十一年（1552）十一月上旬、上杉軍は宇佐美駿河守・柿崎和泉守ら先陣五千余人を川中島へ押し出し、千曲の川端に進んで三段に構えた。大将<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は、会［あい］村（長野市篠ノ井会）に布陣し、総勢合わせて三万余人、三十六備えである。その場所は、千曲川左岸に接した上横田・下横田・小森（長野市篠ノ井）である。</p>
            <p>　上杉軍の出陣の知らせを受けて、甲府の大僧正<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公も出馬し、総勢は一万八千余人。御本陣は千曲川の南、<a href="https://kawanakajima.nagano.jp/tour/tour-176/">雨の宮</a>である。矢代（屋代）より<a href="https://kawanakajima.nagano.jp/tour/tour-176/">雨の宮</a>、土口、笹崎、岩野山は、みな武田の陣場となり、備え二十一カ所である。</p>

			<h4 class="c-heading__s">往古より知られる雨の宮の地</h4>
			<p>　<a href="https://kawanakajima.nagano.jp/tour/tour-176/">雨の宮</a>は、古くからあったと思われる。宗祇［そうぎ］法師（室町後期の連歌師）が行脚中、信濃に立ち寄ったとき、このあたりは大変な干ばつだった。困っていた人々は宗祇の袂にすがり、雨乞いの歌を願った。</p>
            <p>宗祇は哀れんで、近くの森に雨の宮が祭られていることを聞き、<br>
            　いかなれば神の御前の草枯るる<br>
            　何を印に雨の宮とは<br>
            と和歌※を詠じられた。その途端に空が曇りはじめて、大雨が車軸を流すように三日間降り続けたという。これ以降、<a href="https://kawanakajima.nagano.jp/tour/tour-176/">雨の宮</a>は有名になったと伝えている。</p>
            <p>※歌碑は、雨宮坐日吉神社［あめのみやにいますひえじんじゃ］（千曲市雨宮）の社地にある。</p>

			<h4 class="c-heading__s">上杉軍、山本重頼の策により、千曲川を渡る</h4>
			<p>　こうして十一月十九日から二十七日までの九日間、両軍互いに川をはさんで、無駄に弾丸と矢を交わすだけで、日は過ぎた。信州は日本一寒い国である。甲州勢の兵の中には、雪と寒さに耐えかねて自国に帰りたいと願う兵も出てきた。<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公も自分の寒気を口には出さず、大将たちを諫めた。</p>
            <p>　一方の上杉方、「我が軍は越後の深雪に馴れているので、いまが合戦の好期。勝ったも同然」という柿崎和泉守景家に対して、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は「味方は雪には強いかもしれぬが、武田軍のように川の合戦には馴れていない。一つの利を見て戦うことは、敗戦のもとである」と仰せられた。</p>
            <p>　「合戦をあまり見合わせては、臆病と言われるのも悔しいこと。また密かに敵の機を察し、速やかにその利に乗って不意を撃つとする太公望の兵勝の術もあります。いずれにしても軍法の触れを回らせるように」と景家は急き立てる。</p>
            <p>　そこで<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は、山本帯刀重頼を召されて相談された。「眼の療治に三里（脚のツボ）に灸をすえるように仕掛けてはいかがなものでしょうか」と重頼は答えた。<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公ははったと手を打って「妙案、妙案」と同意された。つまり、はるか脇より千曲川を渡り、敵の後ろにまわり、前後より討つ戦法。柿崎和泉守も「あっぱれの軍法」と舌を振るった。</p>
            <p>　暮れゆく霜月二十七日の夜、上杉軍は、宇佐美駿河守、甘粕近江守らの面々を先陣に、千曲川の白鳥河原を渡って矢代の上へ、一重山に続いた有明山を越えて、森（千曲市森）の地に陣を構えた。</p>

			<h4 class="c-heading__s">夜回りの長尾越前守、佐藤と深堀の太刀を奪う</h4>
			<p>　<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>の姉婿の長尾越前守政景は、前々から腹黒く、邪心ある人物だった。その夜、陣まわりをしていた長尾越前守は、寝入っている佐藤主計・深堀藤四郎という勇士の太刀を奪い取ってしまった。</p>
            <p>　翌朝、二人は太刀のないことに気づいた。面目なく、深堀は切腹しようとしたが、佐藤は「戦場に出て主君の役に立つことなく死ぬ法はない。明日の戦いを待たれよ」と、流れる涙を拭うことなく諫めた。佐藤の忠告に深堀は死ぬことを思い止めたのだった。</p>

			<h4 class="c-heading__s">信玄公と小幡入道、かすかな気配に敵の動きを知る</h4>
			<p>　<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は油断なく、自ら日に三度ずつ陣まわりをして警戒していたが、二十八日の暁天、有明山より鹿が群がって武田の陣所を横に笹崎の山へと駆けていった。</p>
            <p>これを見た　<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は、<a href="https://kawanakajima.nagano.jp/character/obata-toramori">小幡入道日意</a>を召し連れ、「鹿は人を恐れ、招けども寄ってこないものだ。昔、源平の戦いでも、軍兵に驚いた鹿や猿が平家の陣所へ落ち下ったという。敵は勢を二手に分け、一手を山手へまわし、前後より引き包んで討とうとする戦術だろう」と告げる。<a href="https://kawanakajima.nagano.jp/character/obata-toramori">小幡入道日意</a>も、「夜回りで千曲川に新しい馬の沓が流れ来るのを見つけたので、敵が川を渡ったに違いないことをご報告しようと思っておりました」という。</p>
            <p>　<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は常に変わらぬ小幡の心がけに感心され、「油断するなよ」と申された。<a href="https://kawanakajima.nagano.jp/character/obata-toramori">小幡</a>は重ねて「味方は小勢で、慣れない寒気と雪で兵たちも心労しております。状況によって戦術を自由に変える大将が良将というもの。不利な戦に兵を痛めることはされますな。ただ今より軍を収め、陣払いするのが最善の策。自分は法師武者、君の身代わりに残って静かに勢を引き上げましょう」と陣払いを進言した。 <a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公はこの儀もっともと、雪の降る中、矢代の方へと引き取った。</p>
