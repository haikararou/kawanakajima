			<h3 class="c-heading__m">二.小幡家由緒のこと</h3>
		</div>
		<div class="c-section__column2__w65">
            <h4 class="c-heading__s">小幡家の由来</h4>
			<p>　小幡の先祖は、上野国甘楽郡小幡（群馬県甘楽郡甘楽町小幡）に住む群馬の音人という。後冷泉天皇の御代、康平年中（1058～1065）、八幡太郎義家（源義家）が東夷征罰の折、上野国に入り、群馬の音人の方に立ち寄ると、空より白絹が舞い降りて庭の竹にかかった。そのさまが旗のようで、義家はこれこそ源氏の白旗ぞと、勝利を確信し、音人に申しつけてその竹を切って旗竿にうち立て、奥州へ下り強敵を亡ぼした。</p>
            <p>　上洛の帰途、音人方を訪れた義家は、「その方が家にて旗を得たことである。よって今よりこの所を小幡と唱え、汝が家の紋を竹の葉にするがよかろう」と申しつけた。これ以後、音人は苗字を小幡とし、紋所に笹の葉を用いることになった。</p>

			<h4 class="c-heading__s">屈辱の佐藤主計、深堀藤四郎、雨の宮に死す</h4>
			<p>　<a href="https://kawanakajima.nagano.jp/character/obata-toramori">小幡入道日意</a>は、備えを万全とし、敵が討ち寄るのを待った。越後方は龍が天に登る勢いで千曲川の端へと繰り出した。その時、寒風がさっと吹き下ろし、空はにわかに曇り、雪で半町先も見えなくなった。さすがの越後兵も川を渡ることなく退いた。</p>
            <p>　このとき<a href="https://kawanakajima.nagano.jp/tour/tour-188/">狗ヶ瀬</a>（『甲陽軍鑑』にいう<a href="https://kawanakajima.nagano.jp/tour/tour-176/">雨の宮の渡し</a>）を渡る二騎の武者がいた。佐藤主計、深堀藤四郎であった。昨夜の恥を注ごうと雨の宮の方へさっと乗りだし、「武田の面々出合って勝負あれ」と大音声をあげ、敵中に割って入り、大勢とわたり合う。しばらく戦って敵六、七騎討ち取るが、まもなく、深手を負い、二人とも討ち死にした。越後方は両人を最後に川を渡る者がなかった。</p>
            <p>　<a href="https://kawanakajima.nagano.jp/character/obata-toramori">小幡入道</a>はこれ幸いと、静かに陣払いする。昨夜山間に隠れていた越後勢は、大山の崩れるごとく、龍の雲水に翻［ひるがえ］るがように小幡隊を猛追した。宇佐美駿河守、甘粕近江守は、敵中に馬を駆け込み、武田軍もこれに応戦した。</p>

            <h4 class="c-heading__s">甲州の勇者八百四人、果敢に戦い、討ち果てる</h4>
			<p>　討死はこの時とばかりに死闘を展開する甲越の両軍。しかし武田方は次第に打ち負け、退いていく。追い詰める越後勢は、「雷鳴のごとく呼ばれた武田の勇者よ、敵に後ろを見せるのか。引き返して勝負あれ！」と声をかける。それを聞いた甲州の八百余人、「ああ無念なり」とたちまち馬の鼻をとって返し、我臆せじ、負けじと戦った。主君のためには、命は鴨毛よりも軽いものぞと惜しまずに戦い、甲州の八百余人は一つ枕に討死した。雪は血潮に紅となり、骸は丘となって重なった。</p>
            <p>　日も巳の下刻（午前十一時頃）となり、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は千曲川を越えて<a href="https://kawanakajima.nagano.jp/tour/tour-176/">雨の宮</a>に移られた。討死した甲州兵を見て、「<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>は良い家臣を持たれたものよ」とつぶやかれ、手厚く弔うことを命じた。それというのも、雑兵にいたるまですべての甲州兵が、仰向けに倒れていたからである。逃げていくものを後ろから切るとうつむきで倒れるが、向かってくる者を切れば、仰向けで倒れるものである。</p>
            <p>　そして、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公が味方の討ち死にしたものを検分されると、三百余人であった。中でも佐藤と深堀が抜け駆けして討ち死にしたことに驚き、それが長尾越前守が夜廻りの時に太刀を奪いとったゆえと知る。言語に絶した非道によって惜しい勇士に無残な最期をさせてしまったと、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は越前守を叱責された。</p>

			<h4 class="c-heading__s">甲州軍、寒気に勝てず、陣を引く</h4>
			<p>　時に<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は、雨の宮を陣払いし、三千余人を引き連れて寂蒔原［じゃくまくはら］（千曲市）に陣を替えた。<a href="https://kawanakajima.nagano.jp/character/obata-toramori">小幡入道</a>は殿（しんがり）を済ませ、陣所に参じた。<a href="https://kawanakajima.nagano.jp/character/yamagata-masakage">山形（県）三郎兵衛</a>は、味方の討ち死にを嘆き「ここより取って返し、一合戦せん」とはやるが、<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄公</a>は押し止め、「このような深雪で味方は寒気に苦しみ、思うようにはならない。弔い合戦は明春にして戦はこれでいったん収め、帰国しようぞ」と仰せられて直ちに帰国した。一方、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公も引き取り凱陣した。<br>
            　雨の宮の合戦で討ち死にした者は、甲州方は九百余人、越後方は三百余人であった。</p>
