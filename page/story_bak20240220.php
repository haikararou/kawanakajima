			<h4 class="c-heading__s">『甲越信戦録』作者覚え書き</h4>
			<p>　「十八公の栄は霜後に一千年の色は雪中に深し」※とは、もっともなことである。関ヶ原の戦い、大坂夏の陣に勝利し、天下を統一した徳川家康。群雄割拠の戦国時代は終わりを告げ、日本は天下泰平の世となった。<br>※補足：十八公とは、松の異字体「枩」となり、徳川松平十八家を暗示。松の美しい緑は、長い年月、霜や雪に耐え抜いたことによりつくられる。それと同じように、戦国乱世を耐え抜いて幕府を開き、繁栄する徳川家をたとえている。</p>
			<p>　元和２年（1616）、家康は逝去し、日光東照宮に祭祀された。やがて三代将軍家光が、過去の記録・日記を見て、諸国の戦いはこと細かに記載されているが、戦国を代表する名将同士が直戦した川中島合戦の実録がない。不満に思った家光は、上杉家に実録を提出するよう求めた。上杉家は細記を改め『甲越信戦録』五巻として献上した。<br>　ところが、それを旧武田方家臣に見せたところ、武田方の『武田三代記』『甲陽軍鑑』『信玄全集』とはずいぶん異なる記述があり、旧家臣たちは異を唱えた。そこで足利11代将軍義澄の末子である南光坊僧正に見せ、意見を求めると、「甲州の書いたものは武田の悪事や非分を覆い隠し、虚談が多すぎるが、謙信は事実と違う記述を嫌ったので、事実相違ない」と家光に述べた。家光は疑いも晴れ、満足したこの書を「実録」として書庫に納めたという。 　本書は、この上杉家の『甲越信戦録』を柱に、武田方の『甲陽軍鑑』や『武田三代記』、また『北越軍記』『本朝三国志』といった12通の版木本、さらに『川中島評判記三巻』『諸家見聞記二十巻』などの6つの写本、合わせて18書に加え、川中島に伝わる伝承をもとに執筆したものである。</p>
