		<div class="c-single__block">
			<h2 class="wp-block-heading">進軍開始</h2>
			<h3 class="wp-block-heading">8/14</h3>
			<ul>
				<li>上杉謙信（合戦当時の名は上杉政虎。以降便宜上謙信と記す）、1万3千の兵を率いて、品のに向け春日三城を発つ。</li>
				<li>謙信、兵糧隊2千を善光寺横山城駐留させる。</li>
			</ul>

			<h3 class="wp-block-heading">8/16</h3>
			<ul>
				<li>上杉本体は可候峠（そろべくとうげ）を通り、妻女山へ向かう。</li>
				<li>謙信、1万1千の兵を率いて妻女山に布陣。</li>
				<li>高坂氏、ノロシ山から「上杉軍出兵」を知らせるノロシをあげる。（甲府まで約150KMを2時間ほどで伝達したという）。</li>
				<li>武田信玄、甲府にて上杉軍の妻女山布陣の報を受ける。</li>
			</ul>

			<h3 class="wp-block-heading">8/18</h3>
			<ul>
				<li>信玄、1万の兵と共に甲府から出陣。味方を加えながら、24日には2万余の兵を率いて川中島方面に至る。</li>
			</ul>

			<h2 class="wp-block-heading">布陣</h2>
			<h3 class="wp-block-heading">8/24</h3>
			<ul>
				<li>信玄、茶臼山に布陣。上杉軍の退路を断ち、様子を見る。</li>
			</ul>

			<h3 class="wp-block-heading">8/28</h3>
			<ul>
				<li>これにより6日間、両軍が対峙</li>
				<li>信玄は上杉方に戦う気があることを確かめると、山本勘助に上杉方の戦意をくじく戦術を練らせた。<br>勘助は、周辺の村々に紙旗を作らせ、夜は山々に数十か所のかがり火を焚かせ、大軍を装った。</li>
				<li>上杉の家臣は「このままでは甲州軍に春日山城に攻め込まれてしまう」と動揺するが、謙信はうろたえることなく吟詠。<br>「信玄が越後に攻めるなら、こちらは甲州を攻めるまで」という大将の剛気に越後兵士たちは士気を取り戻す。</li>
			</ul>

			<h2 class="wp-block-heading">進軍</h2>
			<h3 class="wp-block-heading">8/29</h3>
			<ul>
				<li>信玄、「これ以上の退陣は無益」として本陣を引き払い、全軍を引き連れ、海津城に入る。</li>
				<li>武田の軍勢は一気に合流して海津城へ入場せず、まず信玄が入城、その後、海津城から遠い部隊より順に入城したと思われる。<br>（集団になって兵をひくと、上杉軍に隙を突かれるおそれがあるため。）</li>
				<li>謙信はこの動きを眼下に見ながら動かず。</li>
				<li>これよりさらに10日間、両軍が対峙。</li>
			</ul>

			<h3 class="wp-block-heading">9/8</h3>
			<ul>
				<li>武田軍、軍議を開く<br>飯富虎昌、馬場信春は「このまま戦わないのは武士の名折れ」と上杉軍との合戦開始を進言。<br>信玄は、馬場信春と山本勘助に評議させ、啄木鳥「きつつき」の戦法を提案。<br>勘助の采配により、部隊を編成する。<br><br>▼妻女山夜襲の啄木鳥隊十将<br>●高坂弾正忠●飯富虎昌●馬場信房●小山田昌行●甘利昌忠●真田一徳斎（幸隆）●相木市兵衛●小山田弥三郎●芦田下野守●小幡尾張守</li>
			</ul>

			<h2 class="wp-block-heading">激戦前夜</h2>
			<h3 class="wp-block-heading">9/9 午後8時ごろ</h3>
			<ul>
				<li>武田軍、兵糧（食事）の用意をする。（海津城より数条の炊煙があがる）</li>
				<li>謙信、炊煙から武田軍の動きを察知。</li>
			</ul>

			<h3 class="wp-block-heading">9/9 午後11時ごろ</h3>
			<ul>
				<li>謙信、100人余りの勇士を残して妻女山を発つ。武田軍の夜襲隊を欺くため、妻女山にかがり火を35か所に焚き、紙旗を立てておいた。</li>
				<li>奇襲に備え、武田軍は順次食事をとっていた。</li>
			</ul>

			<h3 class="wp-block-heading">9//10 午前1時ごろ</h3>
			<ul>
				<li>武田別働隊（啄木鳥隊）1万2千名余が、妻女山の上杉軍を挟撃するべく、海津城を発つ。</li>
			</ul>

			<h3 class="wp-block-heading">9/10 午前3時ごろ</h3>
			<ul>
				<li>甘粕隊1千名が東福寺に留まり、妻女山攻撃隊に備える。</li>
				<li>直江隊1千500名と小荷駄隊500名は横山城方面へ進軍。<br>※小荷駄隊（こにだたい）＝食料、弾薬や馬糧などを運ぶための専門部隊のこと。</li>
			</ul>

			<h3 class="wp-block-heading">9/19 午前4時ごろ</h3>
			<ul>
				<li>信玄、8千名の兵と海津城を出発。</li>
				<li>啄木鳥隊主力隊は尾根伝いに進軍。</li>
				<li>上杉軍本隊は八幡原近くへ進軍、着々と戦いに備えていた。</li>
			</ul>

			<h3 class="wp-block-heading">9/19 午前5時ごろ</h3>
			<ul>
				<li>未明、川中島一帯に霧が発生。</li>
				<li>上杉軍、陣営を整えながら待機。</li>
				<li>直江隊1千500名丹波島に留まる。</li>
				<li>武田本隊、順次川中島八幡原「はちまんばら」に到着、本陣を張る。</li>
				<li>小荷駄隊500名善光寺横山城入城。</li>
			</ul>

			<h3 class="wp-block-heading">9/10 午前6時ごろ</h3>
			<ul>
				<li>上杉軍、車懸りの戦法で武田本陣に近づく。</li>
				<li>信玄、霧の中に人馬の音がするのを不審に思い、濃い霧の中浦野民部左衛門を偵察に出す。武田軍本隊、鶴翼の陣を展開し始める。敵兵がいることに気づき、武田軍は浮き足立つ。</li>
			</ul>

			<h3 class="wp-block-heading">9/10 午前7時ごろ</h3>
			<ul>
				<li>啄木鳥隊、妻女山に奇襲をかけるが、敵陣に謙信はおらず。</li>
			</ul>

			<h3 class="wp-block-heading">9/10 午前7時半ごろ</h3>
			<ul>
				<li>濃い霧で陣形が整わない武田軍に上杉軍が奇襲攻撃を始める。</li>
				<li>武田軍本陣、体制を整えようとするが、抑えきれず乱れる。</li>
			</ul>

			<h3 class="wp-block-heading">9/10 午前8時直前</h3>
			<ul>
				<li>啄木鳥隊は慌てて岩野へ下り、十二ヶ瀬から千曲川を渡ろうとするが、上杉軍・甘粕隊に阻まれて苦戦。</li>
				<li>高坂隊は海津城への奇襲を危惧し、引き返す。</li>
			</ul>

			<h2 class="wp-block-heading">激突</h2>
			<h3 class="wp-block-heading">9/10 午前8時ごろ</h3>
			<ul>
				<li>八幡原一帯で甲越両軍が激突</li>
				<li>霧が晴れ、上杉軍は車懸りの戦法で一気に武田軍目指して襲い掛かる。</li>
				<li>甘粕隊に行く手を阻まれた啄木鳥隊は、戌ヶ瀬、猫ヶ瀬へも移動し始める。</li>
			</ul>

			<h3 class="wp-block-heading">9/10 午前8時半ごろ</h3>
			<ul>
				<li>啄木鳥隊、戌ヶ瀬、猫ヶ瀬から千曲川を渡り、本陣に向かう。</li>
				<li>武田信繁討ち死に</li>
				<li>山本勘助討ち死に</li>
				<li>諸角豊後守討ち死に</li>
				<li>啄木鳥隊が上杉軍の背後を突きはじめる。</li>
			</ul>

			<h3 class="wp-block-heading">9/10 午前9時30分ごろ</h3>
			<ul>
				<li>啄木鳥隊が八幡原付近へ続々と到着。</li>
				<li>上杉謙信、武田信玄の本営に斬りこみ、信玄と一騎打ちを果たす。</li>
				<li>高坂隊、2千の兵を連れ、八幡原へと加勢に向かう。</li>
			</ul>

			<h3 class="wp-block-heading">9/10 午前10時～11時</h3>
			<ul>
				<li>信玄、八幡原の本陣を川向こうの高畑に移す。高坂隊は本陣に到着、戦況を確認。</li>
				<li>陣場河原付近<br>新手の大軍に追撃ちされた上杉軍は総崩れとなり、徐々に犀川方面へ退却。<br>陣場河原（稲里中氷鉋）で行き場を失い、武田軍に多くの兵が討たれる。<br>上杉軍の甘粕隊を追って、武田軍の高坂隊は甘利（昌忠）・飯富と共に丹波島へ。地理に詳しい利を活かして甘粕隊よりも先に丹波島へ到着し、甘粕を迎え撃つ。</li>
			</ul>

			<h3 class="wp-block-heading">9/10 午後12時</h3>
			<ul>
				<li>甘粕隊、なんとか丹波島を渡り、市村に着陣。</li>
				<li>直江隊、旭山の麓に布陣。甘粕隊と合わせて5千が後詰めとして武田軍に備えた。</li>
				<li>謙信は戦乱の中、千曲川を渡り牧島の川辺に着く。</li>
			</ul>

			<h3 class="wp-block-heading">9/10 午後4時近く</h3>
			<ul>
				<li>武田軍、陣場河原で勝どきを上げ、海津城に引き揚げる。</li>
				<li>謙信は従者とともに川田（若穂）から山道を通り、北走。山田越えし、小菅権現の導きで間山より木島に出て、安田の渡し場（綱切の渡し）より富倉峠を越え、越後に戻ったといわれる。</li>
				<li>直江隊、甘粕隊はこのあと3日間その地にとどまり、越後へ引き上げていった。</li>
				<li>永禄4年(1561)第4次川中島の戦いの犠牲者は、「大日本戦史」によると武田軍、死者4千600余名、負傷者1万3千名、上杉軍、死者3千400余名、負傷者6千余名。<br>甲越あわせて3万2千名の約83%にあたる2万7千余名が死傷したといわれ、日本の戦国史上、最大の激戦の一つに数えられるようになった。<br>この戦い以降、北信濃の武将のほとんどが武田方につき、川中島・善光寺平は武田の実質的支配下となった。</li>
			</ul>
