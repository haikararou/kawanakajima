			<p>　保科・<a href="https://kawanakajima.nagano.jp/character/sanada-yukitaka">真田</a>は、敵に後ろを見せるものかと取って返して勇猛を振（る）って八方にまわり切り立てれば、さしもの越後勢もしどろになって引き返す。</p>
