			<h4 class="c-heading__s">高坂弾正、村上勢の勢いに「逃げ弾正」となる</h4>
			<p>　天文二十三年（1554）八月十八日の朝、越後方の者たち五、六十人が馬草を苅りに出た。この時、武田方の<a href="https://kawanakajima.nagano.jp/character/kousaka-danjo">高坂弾正</a>の陣から雑兵が出て、鎌を奪い取り、二、三十人打ち殺し、残る者も半死半生にして引き取った。</p>
			<p>　これを見た<a href="https://kawanakajima.nagano.jp/character/murakami-yoshikiyo">村上義清</a>は大いに怒り、「<a href="https://kawanakajima.nagano.jp/character/kousaka-danjo">高坂弾正</a>はしたなし、人倫をふみにじる奴よ。元来、源五郎という農夫にて、武士の道を知らぬ。予が片腕であった額岩寺を欺いて首をはねた憎い奴め」と、高梨・須田はじめ五百余人、村上の紋どころの丸上の旗を指し上げて踊り出て、<a href="https://kawanakajima.nagano.jp/character/kousaka-danjo">高坂</a>の雑兵を中に取り込み散々に相戦う。積もる恨みのことなれば、<a href="https://kawanakajima.nagano.jp/character/murakami-yoshikiyo">義清</a>は冑（かぶと）を傾け、真一文字に春の雲雀の登るように切り込んだ。この戦いぶりを見て、井上・綿内の面々も我劣らじと、槍先を突き立て切りまくる。</p>
			<p>　さすがの<a href="https://kawanakajima.nagano.jp/character/kousaka-danjo">高坂</a>も突き立てられ、千曲川へと敗走した。この時より<a href="https://kawanakajima.nagano.jp/character/kousaka-danjo">高坂</a>を「逃げ弾正」と異名する。村上の働きに武田の先手が危うくなり、二の目に控える<a href="https://kawanakajima.nagano.jp/character/sanada-yukitaka">真田弾正忠幸隆</a>、保科弾正忠正直らが<a href="https://kawanakajima.nagano.jp/character/kousaka-danjo">高坂</a>を助けようと横合いから突き懸（かか）り、<a href="https://kawanakajima.nagano.jp/character/murakami-yoshikiyo">義清</a>を目がけ鉄砲を放ち、矢を飛ばした。<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公はこの有様をご覧になって、「あれ！ 者ども、村上は我が客なり、討たすな！救え」と下知（げじ）した。</p>

