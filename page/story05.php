			<h4 class="c-heading__s">甲州軍の面々、上杉に対して計略をめぐらす</h4>
			<figure><img src="<?php echo get_template_directory_uri(); ?>/assets/img/story/aun_suzume.gif" alt=""><figcaption>竹に阿吽の雀</figcaption></figure>
			<p>　三尺の剣の光は、氷手にあり。一張の弓の勢いは月心に当たるとは、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">上杉謙信</a>公のことであろう。竹に阿吽（あうん）の雀※の陣幕を張り、黒地に金にて菊桐の旗を押し立てた<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公の御陣営には、六匁玉の鉄砲が二百挺。各将たちが堂々として備え、鳴りを静めて控えた。</p>
			<p>　時に、甲州の軍奉行の面々は、「去年十一月、雨の宮の戦では味方の負けとなったが、今度はこちらが計略をめぐらせ、敵を悩ませてはいかがなものだろうか」と評議した。すると<a href="https://kawanakajima.nagano.jp/character/yamamoto-kansuke">山本勘助道鬼</a>が、「千曲川の端に一丈ばかりの葭（あし）が茂り、日の影も通らず、伏勢するには好都合の場がある。五百人づつを七か所に伏せて、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>をここまで追い出し、雌雄を決しよう」という。</p>
            <p>　<a href="https://kawanakajima.nagano.jp/character/yamagata-masakage">山形（山県）三郎兵衛</a>は、「七か所に伏勢するよりは、よい一か所を選んで伏兵を差し置いた方が利があろう」という。<a href="https://kawanakajima.nagano.jp/character/yamamoto-kansuke">山本</a>は頭を振り、「いやいや、義経の軍法は、百首の中に百人を十か所に置く備えこそ、千の敵にも切り勝つと教えておられる」というので、皆々この意見に賛同した。</p>
            <p>　しかし、「どのようにして大将の<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>を引き出すのか」といえば、<a href="https://kawanakajima.nagano.jp/character/yamamoto-kansuke">山本勘助</a>は、「<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>は生来剛力で、武術の早業は達人。よい大将と見れば、自身の働きを好まれる。<a href="https://kawanakajima.nagano.jp/character/takeda-nobushige">典厩</a>殿を先に立てれば、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>の出馬は間違いなし。味方は一応戦うふりをして逃げて見せれば、上杉殿追い来るは必定。この時、伏勢をもって討ち破ろうぞ」という。各々、「さすが<a href="https://kawanakajima.nagano.jp/character/yamamoto-kansuke">勘助</a>殿、あっぱれ神変の術」と感心した。御本陣の<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公もはなはだ感心され、「急ぎ出陣すべし」と下知した。</p>
            <p>※竹に阿吽の雀……竹に飛び雀。口を開いた雀と口を閉じた雀を表した上杉氏の家紋。</p>

			<h4 class="c-heading__s">典厩信繁、謙信を誘い出すべく先陣に立つ</h4>
			<p>　命により、御舎弟・<a href="https://kawanakajima.nagano.jp/character/takeda-nobushige">典厩信繁</a>が先陣の采配を取った。随（したが）う人々には、<a href="https://kawanakajima.nagano.jp/character/baba-nobufusa">馬場民部少輔</a>、小笠原若狭守、板垣弥次郎、室賀出羽一葉軒、跡には<a href="https://kawanakajima.nagano.jp/character/yamagata-masakage">山形（山県）三郎兵衛</a>、跡部大炊介、<a href="https://kawanakajima.nagano.jp/character/yamamoto-kansuke">山本勘助入道</a>ら、かれこれ五千余り。上杉方は斥候の知らせを受け、わざと鳴りを潜めた。</p>
			<p>　武田方先駆けの<a href="https://kawanakajima.nagano.jp/character/baba-nobufusa">馬場</a>、小笠原は、何の会釈もなく鯨波の声（ときのこえ※）を発して向こう備えに切って懸る。待ちうけていた上杉方は、用意の陣筒二百挺を一斉に撃ち放った。武田の先手は、たちまち血煙立って、浮き足となる。そこで<a href="https://kawanakajima.nagano.jp/character/yamamoto-kansuke">山本勘助</a>は「数代のご厚恩も一日のご用にたてんためではないか。主君の報恩に討死し、名を後代に残そうぞ！」と、声を嗄（か）らして味方を励ました。そのかけ声に、甲州勢にわかに勇気沸き起こり、力足を踏んで戦う。</p>
            <figure><img src="<?php echo get_template_directory_uri(); ?>/assets/img/story/tenkyu.gif" alt=""><figcaption><a href="https://kawanakajima.nagano.jp/character/takeda-nobushige">武田典厩信繁</a></figcaption></figure>
            <p>　血気盛んな<a href="https://kawanakajima.nagano.jp/character/takeda-nobushige">武田典厩信繁</a>、馬を進め、太刀抜きかざして戦う。大将軍を敵に見られまいと、小笠原・板垣・室賀は<a href="https://kawanakajima.nagano.jp/character/takeda-nobushige">典厩</a>とかけ離れて相戦う。この戦いの場が、南原の東側砥部の南向こう、いま合戦場といわれるところで、俗に<a href="https://kawanakajima.nagano.jp/tour/tour-245/">勘助宮</a>（長野市 南長野運動公園一帯の地）という。</p>
            <p>※鯨波の声……ときのこえ。げいはのこえ。大勢が一斉に発する声</p>

			<h4 class="c-heading__s">勘助の計略にかかり、謙信公、八方を囲まれる</h4>
			<p>　<a href="https://kawanakajima.nagano.jp/character/takeda-nobushige">典厩</a>と見ると<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公はみずから切って出られた。従う馬まわりには強力の士三十二騎。上杉兵は<a href="https://kawanakajima.nagano.jp/character/takeda-nobushige">典厩</a>目懸けて切りかかり、両軍入り乱れ、双方討たれる者数知らず。大山をも脇挟む勢いの甲州勢も、終に打ち負けて敗走しはじめる。上杉軍はこれを追い詰め、千曲川まで追っていく。</p>
			<p>　<a href="https://kawanakajima.nagano.jp/character/yamamoto-kansuke">山本勘助</a>は、計略通りと合図の陣鐘をつき鳴らせば、「えいえい、わいわい」と七か所より不意に顕れ出た伏勢、三千余人八方より隙間もなく鉄砲を撃ちかけ取り潰す。上杉方は網代の魚のように逃げ場なく見えた。大将を囲んで討たれる者七百余人。武田方は<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>目懸け、突っ懸け挑みかかったので、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公すでに危うく見えた。</p>

            <h4 class="c-heading__s">鬼小次郎助宗、甲州勢を寄せつけず</h4>
			<p>　その時、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公の向こうに只一騎、鬼小次郎助宗が、稲妻のごとくに太刀を振り立て、一世の大事ここにありと駆け寄った。その果敢な戦いぶりは、仁王の荒れくるったように駆けまわり、八方に眼を配って敵を寄せつけず。血は顔面に流れて、阿修羅のような形相となり、いや増す勇気ふんぶんの鬼小次郎を武田軍は取り囲んだ。</p>
			<p>　<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公も甲州勢をものともせず、自ら囲みを切り抜け切り抜け、神明の森（篠ノ井神明・富部御厨岡神明神社）へ馬を飛ばす。上杉軍は大崩れして<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公を見失って、丹波島の方へ後退した。鬼小次郎ばかりは、君を案じ、悪鬼を押さえる鍾馗（しょうき）のごとき奮戦である。武田方は、大将を討ちもらし、残念と神明の方へ追って行く。</p>
            <p>　<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は、主従五騎にて犀川の方へ落ち行くが、追いすがる武田軍は、津波の押し寄せるようであった。</p>
    
			<h4 class="c-heading__s">宇佐美・山本、押し寄せる敵勢から主君を守る</h4>
			<p>　時に大塚、小島田の間に後陣していた宇佐美駿河守、山本帯刀両人は、君を迎えて守り、八百余人丸備えになって敵を待つ。</p>
