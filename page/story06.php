			<p>　家臣一同その儀に応じ、戸神山へ出馬した。誠になにもない上野原は、四方が皆、山また山である。<a href="https://kawanakajima.nagano.jp/tour/tour-180/">飯縄山</a>（標高1917.4m）はそびえて天に近く、清水岩を叩いてみなぎり落ちる閑曲の地である。甲州勢も陣を堅くし、柵を構え、要害厳重にして互いに夜討ちを戒め、対陣はいつ終着するかわからない状態が続いた。</p>       
			<p>【※補足】<br>
            髻山は、戸隠からは<a href="https://kawanakajima.nagano.jp/tour/tour-180/">飯綱山</a>（飯縄山）を挟んだ東方にあるので、話のように両軍が対陣するには位置的に無理があります。弘治３年（1557）８月の川中島の戦いにおける「上野原の戦い」は、<a href="https://kawanakajima.nagano.jp/tour/tour-436/">髻山城</a>近くの若槻上野原（うわのはら）と、戸隠上野原（うえのはら）の両説があり、いずれか一方が戦場ならば辻褄があうのですが、「甲越信戦録」の作者（もしくは写本の段階で）も混同したのではないかと考えられます。この原稿ではほかの箇所同様、あくまで物語として地理的な隔たりは無視し、原文どおりで掲載しました。</p>

        <h4 class="c-heading__s">信玄公、忍びの報せを聞き、敵陣の火事を予見す</h4>
			<p>　時に、八月二十三日、甲州の忍びの者が上杉勢の陣所を窺って本陣に戻り、「不思議なことは、上杉方は小荷駄をつけ出し、人夫までも立ち騒ぐ様子。どうやら将軍の陣払いでござろう。越後に大事件発生と見えまする。急ぎ兵をまわし、敵の返り路を遮（さえぎ）れば、味方は勝利するはず」と注進した。これを聞いた<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公、からからと笑われ、「今明晩の内に敵陣から出火があったとしても、けっして味方の勢一人も攻めかかってはならぬ」と厳命した。</p>
			<p>　<a href="https://kawanakajima.nagano.jp/character/kousaka-danjo">高坂弾正</a>は、この旨を味方に触れまわった。士卒らは「火事を前もって知る人などおられようか」といっていたが、ある士卒は、「東福寺（京都市東山区にある臨済宗の寺）の長典子※（ちょうでんす）が、ある時、廊下より西の空を詠（なが）め、『唐の国でやがて火事があるだろう。我はこれを救わねばならぬ』と一瓶の水を西へ投げられた。翌年、唐の育王寺より使いが渡来して、『去年の出火の節は、お救い下され、忝（かたじけな）く候』と、瑪瑙（めのう）の卓をお礼にと持参したという。こうした例もあることから、<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公も未然を察知されたのかも知れない」と申した。</p>
			<p>※長典子＝兆殿司（ちょうでんす）。吉山明兆のこと。室町時代初期の画僧。東福寺の殿司の役にあったので兆殿司といわれた。宋・元の画法を合わせた強い筆致が特徴。</p>       

        <h4 class="c-heading__s">敵陣に火の手あがるも、お触れに随い甲州勢動じず</h4>
			<p>　翌晩二十四日、すでに夜が更け渡るころ、<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公が申された通り、越後の陣から火の手が上がった。火煙は八方に舞い広がって天を焦がす勢いである。これを見た甲州勢は勇み立ち、敵の出火は味方の吉事、敵陣へ切って出ようとひしめきあった。しかし、先達（せんだっ）ての御触れはきっとこの事に違いないと思い出し、ただ遠見して見守った。</p>
			<p>　翌朝、上杉方を見ると大鳥が翅（翼）を開いたように左右に分かれて布陣している（鶴翼の陣）。そして山のように薪を積んで焼き、甲州勢が来たら引き包んで討ち取ろうと待ち構えていたことがわかった。<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は前もってこれを悟っていたので、一人も出撃させなかった。上杉方の計略は水の泡となった次第である。</p>

        <h4 class="c-heading__s">信玄公、負けじと秘計をめぐらす</h4>
			<p>　さて、<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は浅ましい<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>めの胸中、今後の戦略もおよそ見当がついたと、相応の戦術で勝利を得て見せようと、大将分を召し連れて申し渡した。</p>
			<p>　「味方二万余人の軍卒のうち、一万は旗本と定め、残る一万余人を五千づつ二手に分け、両側の林に隠れよ。そこへ、馬三十疋（ひき）を手綱を切って敵陣の方へ放し、雑兵どもがこれを捕えようと追っていく体に見せ、馬をたたき立て、敵の方へ追い出せ。越後勢が馬を取ろうと備えを乱して崩れ立つところを、二つの伏勢が落ち合って追っ取り込み、一人も余さず討ち取るべし。向こう備えの者どもも一同に攻めかかれば、必ず切り崩すことができよう」。 皆々はこの戦術に「あっぱれ孔明※の秘計」と感じ入った。</p>
			<p>※孔明＝諸葛亮（しょかつりょう）。孔明は字（あざな）。中国、三国時代、蜀漢の名宰相。劉備に「三顧の礼」を受けて仕えたと伝えられ、天下三分の計を説き、蜀漢の建国を助けた。歴史小説『三国志演義』においても、信義に厚く、知略に優れた天才軍師として描かれている。</p>       

        <h4 class="c-heading__s">謙信公、武田の計略を見破り、両虎相砕くが如し</h4>
			<p>　こうして二十六日の朝より静かに勢を伏せさせ、夕方になって馬三十疋（ひき）を敵の方へ放した。昔、聖徳太子が甲斐の黒駒を召し連れたほど、甲州は全国有数の名馬の産地である。越後勢は武田の計略とも知らず、我先にと甲州の馬を奪い取ろうと陣所々々一同に騒ぎ出した。</p>
			<p>　山の半腹よりこれを見下していた<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は、計略を見破り、「決して馬に気を奪われず、備えを崩すなと触れるべし」と宇佐左馬介、和田兵部に下知された。二人が「敵の馬に目を懸けるな！味方の馬は出羽（でわ）の出生にて敵に劣りはせぬ。備え崩さず相守れ！」と馬上にて駆けまわったので、皆々静まった。</p>
			<p>　武田方は越後勢の透間を待ったが、ついに備えは乱れなかった。蘇子の書に、「智をもって智を攻め、勇をもって勇を撃つ。両虎相砕くが如し。歯牙気をもって相勝つことなし」と述べているように、互いの計略は不成功に終わり、両陣営は対陣したまま退屈の思いを募らせた。</p>       

        <h4 class="c-heading__s">使者鬼小島弥太郎、信玄公の猛犬を軽々と圧殺す</h4>
			<p>　こうしたところに越後からの使者が上野原の陣所に来た。「京都将軍家よりの御召し。急ぎご帰国されたし」との知らせであった。これによって<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は、武田の陣へ鬼小島弥太郎一忠を使者として遣わした。<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公の口上は、<br>
            「この度、京都将軍義輝公の御召しにより上洛仕（つかまつ）り候。このゆえに、軍勢を越後へ引き取り候。勝負を決せんと思われるならば、貴殿方より戦を始めなされて結構。ただし、帰国まで戦会を御延引くだされるよう」と、弥太郎一忠は、敵中恐れず、<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公の御前でも少しもひるむことなく、堂々と口上を申し述べた。</p>
			<p>　そんな弥太郎の大勇を憎む武田の軍兵は、<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公ご秘蔵の「獅子」という唐犬をけしかけた。犬は口上を述べる弥太郎の右の膝へ食いついた。だが弥太郎は少しも顔色を替えずに口上を述べながら、彼の犬をちょっと押さえただけで、犬はそのまま鳴き声も立てずに死んでしまった。人々は弥太郎の強力、沈着に驚くばかりだった。</p>
			<p>　<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は口上に対し、「仰せの趣、御念の御儀、戦はこの度に限らぬ所存。拙者も甲州へ帰陣し保養仕ろう」と返答し、双方ようやく和睦が成立した。互いに旗を巻き、馬印を伏せて両大将は馬上にて、冑、甲を脱いで式礼されて別れた。士卒も互いに目礼し、左右に別れて帰国した。</p>       
