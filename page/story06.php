		<div class="c-section__column2__w65">
        <h4 class="c-heading__s">戸隠、戸神山の上野原</h4>
			<p>　当国、信州に紛らわしい地名がある。「上野」である。「ウハノハラ」という時は、小県で、上田東伊勢山（上田市上野地区、砥石城跡のあたり）である。「ウワノハラ」という時は、水内郡で、吉田稲住（長野市若槻・上野・稲田地区のあたり）の方である。「ウエノハラ」という時は、戸隠山中（長野市戸隠）である。</p>
			<p>　読み方によって方角はたいへん相違する。上杉秘録『五戦記』に、弘治二年（1556）<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公が信州に出て戸神山に陣すという。これは弘治三年（1557）のことである。信州に戸神山を知っている人は希（まれ）である。</p>
			<p>　戸隠山は、一名「戸神山」という。神代の時、手力雄尊（たぢからおのみこと）が、天の岩戸を引き放して、お投げになられたところ、この峰に止まったことから、戸隠山（標高1904m）という。戸をお放ちになられた神を祀るゆえに、戸神山という。</p>
        
        <h4 class="c-heading__s">謙信公、信州へ出馬し、髻山の山中に陣を置く</h4>
        <figure><img src="<?php echo get_template_directory_uri(); ?>/assets/img/story/motodoriyama.jpg" alt=""><figcaption>髻山城を望む</figcaption></figure>
			<p>　弘治三年（1557）八月、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">上杉謙信</a>公は信州へ出馬した。大田切・小田切※１との間から戸隠山の北、院苗の滝※2を見て、上野原より<a href="https://kawanakajima.nagano.jp/tour/tour-436/">髻（もとどり）城</a>※3に本陣を置いた。総勢一万五千余人。空濠を構え、柵を囲い、長陣の用意だったが、越後勢はこうした山中の陣を不思議に思い、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公の心中を推察できなかった。</p>
			<p>　<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は<br>
            「孫子の謀攻篇には、『百度戦い、百度勝ったとしても、善の善たるものではない。戦わずして敵兵を屈することこそ、善の善たるものである』と述べている。予がここに陣を構えたのは、三つの大事を思うからである。<br>
            　 第一は、この所は、味方が馳せ行く自在の地である。<br>
            　 第二は、この所は、敵方より夜討ちの忍び入ることができぬ場所である。<br>
            　 第三は、この所は水の手がよく、敵が火をかけることのできぬ所である。<br>
            　 これらの損益を考えて、敵をここに誘い出し、戦おうとするためである。譬（たと）え一合戦がないとしても、対陣して敵を屈するには最良の所である。義経軍法百首にも、『攻めもせず、戦いもせず、義をもって、すべて勝つは、善の善である』とも記している。勝つ手は駆け引きによる。もし敵が来たら、敵に退屈させ、不意にかかろうぞ」 と仰せになり、兵糧と薪を山のように積ませた。</p>
			<p>※１）小田切・大田切……いずれも新潟県妙高市田切のあたりと思われる。<br>
            ※２）院苗の滝……苗名の滝（新潟県妙高市杉野沢）と思われる。<br>
            ※３）長野市浅川西条と上水内郡飯綱町牟礼との境にある髻山山頂に築かれた山城。<a href="https://kawanakajima.nagano.jp/tour/tour-436/">髻山城</a>。</p>       

        <h4 class="c-heading__s">甲州勢、勝負を決しようと戸隠山の麓に対陣す</h4>
			<p>　<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公出馬の飛札（ひさつ）が甲州へ届いた。<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は躑躅（つつじ）ヶ館を出馬し、<a href="https://kawanakajima.nagano.jp/tour/tour-212/">海津</a>へ入城した。兵は信濃各地から相加わって総勢二万三千余人となる。</p>
			<p>　小山田備中守、<a href="https://kawanakajima.nagano.jp/character/kousaka-danjo">高坂弾正</a>は御前にまかり出て、「この度の上杉勢は川中島に出陣せず、戸隠山の間、上野原※に陣取りしたとのこと、その意図理解できかねまする」と述べた。これに対し、<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公、「<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>はさぞや一ッの利を見つけて、上野に陣取りしたのだろう。どれだけの妙計があるにせよ、敵は客戦、味方は主戦。この方より押し寄せ、勝負を決しようぞ」と仰せられた。</p>
