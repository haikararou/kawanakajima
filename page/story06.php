			<h3 class="c-heading__m">二.謙信公上洛のこと</h3>
		</div>
		<div class="c-section__column2__w65">
        <h4 class="c-heading__s">謙信公、管領職を継ぎ、将軍に謁見のため上洛する</h4>
			<p>　そもそも、足利将軍家と申すは、清和天皇より十七代目の皇孫足利高氏、のちに尊氏と書き改め、征夷大将軍となられた。三代目の将軍義満より公方の尊号を勅許される。これより公方と称し奉る。十三代目、参議左中将義輝公は、京都室町に唐の宮殿のような御所を建てたが、当将軍の不徳であろうか、諸国の動乱は止まず、諸侯の拝礼も上洛も絶えて、将軍はあってないようなものであった。</p>
			<p>　<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公のこの度の上洛は、兼ねて上杉憲政より譲り請けた管領職について、将軍への拝謁を許されたゆえである。<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は、北国通りをして、都に到着。室町詰衆に上洛の届けをするため、鬼小島弥太郎を使者に遣わした。
</p>

			<h4 class="c-heading__s">将軍ご寵愛のお猿様と鬼小島弥太郎</h4>
            <p>　義輝公はたいへん武術をお好みになられ、ご近習、お小姓に武術の稽古を仰せつけては、ご覧になられた。ところで、御所には人の真似をすることが上手な年を経た猿が飼われていた。お小姓の立ち会いにも木太刀をもって交じることがあったが、元来身軽なこともあり、人間にはこの猿に叶う者はいなかった。義輝公はこれをお慰めにご覧になられ、近習が猿に打ち負ける時などはご機嫌であった。しかし、この猿、将軍のご寵愛をいいことに、後には手におえず、初めて御所へ上がる者を見ると刀を奪い、木刀を持ってかかってくるので、困惑する者も多かった。しかし、人々はお猿様と称して大切に飼育していた。</p>
