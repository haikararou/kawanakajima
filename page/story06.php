			<h4 class="c-heading__s">謙信公、上洛につき、斎藤下野守を甲州に遣わす</h4>
			<p>　年月に関守（せきもり）なく、この年、永禄四年（1561）となる。<br>
			　<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は去年産出した佐渡の黄金を禁裏（天皇）へ五箱、将軍へ五箱献上された。そしてこの春、再び上洛するため、甲州へ使者を遣わした。使者は斎藤下野守則忠である。他国の使いを勤める者は、弁舌の才覚がなくては難しい。</p>
			<p>　斎藤下野守は、『史記』に語られる齊王の使者　淳于坤（じゅんうこん）に劣らぬ才智にたけた者である。世に「富樓那（ふるな・釈迦十大弟子の一人。雄弁で説法第一と称せられた）の斎藤」と名高い。この斎藤下野守は、背丈が低く、一眼の醜男であった。</p>

			<h4 class="c-heading__s">富樓那の斎藤、武田曲渕・初鹿野・跡部をやりこめる</h4>
            <p>　ほどなくして斎藤は甲州の館に参上した。出迎えの曲渕庄左衛門（まがりぶちしょうざえもん）、初鹿野伝左衛門（はじかのでんざえもん）が、斎藤を通して、四方山話のあと、「越後は海七分の小国と承ってござるが、しかし、大国でござろうや」と尋ねた。斎藤は、にやっとして、「仰せの通り、海を帯びて六十里程度の小国でござる。甲州はいかほどのお国でござろうや」と問い返す。</p>
            <p>　曲渕は、「当国は南北八日路でござる。大国の証には、日々馬が千匹づつ通ってござる」という。 すると斎藤、「それははなはだ小国でござるな。馬千匹に積む荷物は、わが国の船一艘分でござる。越後は日々大船千艘づつ出入りいたす。さて、甲州は駿河より分かれた僅かに四郡の小国。昔、諸国より貢馬（こうば・くめ）※というものがあって、八月十五日に相坂山（逢坂山、大津市）まで牽いてくるのに、甲州と信州とは、その道八日路と定めている。都までさえ八日路なのに、どうして甲州ばかりを八日路とするのか、その意、理解できかね申す」と笑った。 　曲渕と初鹿野はやりこめられて引き下がってしまった。</p>
			<p>　その後へ、跡部大炊助（あとべおおいのすけ）がやって来て、「いかに斎藤殿、越後では他国への使者に貴殿のような小兵ばかりでござるか。毎度見えられたお使者はいずれも小兵でござる。甲州では、使者は勇を選び、男を選んでござるよ」といったところ、斎藤は、 「越後でも使者については、男をよく選んでござる。大国へは大男を、小国へは小男を選んでござる」と切り返した。 跡部もいいまくられ、恥をかかせられ退散した。</p>
			<p>※貢馬……中世、宮廷に献上された馬。平安時代には、毎年８月に各国の牧場から京に届けられた貢馬を天皇が御覧になる駒牽（こまひき）の儀式が行われた。</p>

 			<h4 class="c-heading__s">信玄公、上洛の野望を語る</h4>
			<p>　しばらく過ぎて<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公、使者の斎藤と対面された。斉藤の謙信公口上は、「この度、拙者またまた上洛仕り候。私用ではなく公務にて、拙者が帰国しご案内を申し入れるまで、戦会はお延ばし下されるよう頼み申し候」と申し述べた。</p>
			<p>　<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公のお返事は、「再びの上洛、ご奇特に存じ候。愚僧も近々上洛の望みあり。まず駿河の今川、参（三）河の徳川、尾張の織田、伊勢の北畑、江州の佐々木ら、道中の者を討ち亡ぼし、都まで自国に致してから京入りしたいものである。よって貴殿ご帰国するまでは相待ち申す。お留守の儀、お気遣いなされますな」といわれた。</p>

			<h4 class="c-heading__s">晏子のような者よ、と信玄公、斎藤を誉め讃える</h4>
			<p>　<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は、「富樓那の斉藤」の噂をご存知であったので、御盃を斎藤に下されてから、意地悪く問われた。「その方、はなはだ小男で、見ると一眼である。知行はいかほどであろうか」<br>
			斎藤は、「六百貫を頂戴しております」という。<br>
			「それは過分の呉れようぞ」と<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公が笑われると、斎藤は意に介せず、<br>
			「武田のご家風は存じ上げませぬが、越後では普代の者と申せば、身障者にても前々の禄は賜ります。拙者の一眼は、片目を射られてもその矢を抜かずに追っ懸けて矢を射返した長尾の先祖、景政に似てれば、武功の印と我が君は悦び、重用してます。御家には、左足が不自由に右眼も潰れておられる山本勘助殿という小兵を、お抱えなられていなさる。そのゆえ我少しも恥じるところはありませぬ」と申し上げた。</p>
			<p>　<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は斎藤の才を「晏子（あんし）※のような者よ」と誉められて、引き出物をお与えになり、斎藤は無難に役目を果たして立ち帰った。 こうして<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は、今は心安しと上洛したのであった。</p>
			<p>※中国春秋時代の斉の名宰相、晏嬰（あんえい）の敬称。</p>

			<h4 class="c-heading__s">信玄公、再び約束たがえ、上杉方の割ヶ嶽城を攻め落とす</h4>
			<p>　<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公の上洛後、一方の<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は、信州筋御仕置きのため、ご出馬された。<br>
			<a href="https://kawanakajima.nagano.jp/tour/tour-212/">海津</a>より<a href="https://kawanakajima.nagano.jp/character/kousaka-danjo">高坂弾正忠昌信</a>がご案内し、水内郡、高井郡をまわる。<a href="https://kawanakajima.nagano.jp/character/kousaka-danjo">高坂</a>は越後境の大田切まで乱入して、民家所々に放火する。ここまで踏み込んだ上は、割ヶ嶽（わりがたけ・鰐ヶ嶽ともいう）の城※も攻め落とすべしとの上意によって攻め懸った。</p>
			<p>　越後方は不意の放火に驚き、かつ卑怯の振舞いと大いに怒り、牙を噛む思いである。武田方の<a href="https://kawanakajima.nagano.jp/character/tada-mitsuyori">多田淡路守</a>、浦野民部左衛門・<a href="https://kawanakajima.nagano.jp/character/hara-toratane">原美濃守入道</a>は先手に進む。<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公より守将に任命された割ヶ嶽図書介は七百人の小勢であったが、大石、大木を甲州勢の上へ落とした。先方衆は数多の戦死、戦傷者が出たが、死を恐れず先懸けを争った。城中一同死者狂いと切って出て、違約した武田の人でなしめと歯ぎしりして奮戦した。</p>
			<p>　甲州勢は苦戦であったが、浦野民部左衛門が搦め手より息をも継がせず攻めるうちに、城中の兵は枕を並べて討死していく。<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は下知して城郭を破却して、勝鬨（かちどき）を揚げ引き取った。この戦いで<a href="https://kawanakajima.nagano.jp/character/hara-toratane">原美濃守</a>は深手を負ったので、甲州へ帰された。</p>
			<p>※割ヶ嶽城（上水内郡信濃町富濃柴津）。永禄４年（1561）５月に大森掃部助が攻落し、<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">武田信玄</a>に所領を安堵されている。なお、安曇郡小岩ヶ嶽城（安曇野市穂高町有明）も割ヶ嶽、または蟻ヶ嶽と称された。</p>

			<h4 class="c-heading__s">「信玄八悪」、仁なくば、お家は亡びる</h4>
			<p>　<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は、日本開闢（かいびゃく）以来の良将で、戦場の働き智勇は兼ね備えの人である。しかし、仁の道は薄かった。これがこの人の欠点である。世で「<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>八悪」といって、一ッに<a href="https://kawanakajima.nagano.jp/character/takeda-nobutora">信虎</a>、二ッに<a href="https://kawanakajima.nagano.jp/character/takeda-yoshinobu">義信</a>、三ッに<a href="https://kawanakajima.nagano.jp/character/suwa-yorishige">頼重</a>、四ッに盲人、五ッに希庵、六ッに大田切、七ッに割ヶ嶽、八ッに身延であるとあげている。</p>
			<p>　ああ、惜しいことよ。仁ないゆえか、<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公一代にして勝頼の代に武田家は亡びてしまった。仁あれば、代替りしても実を生じ、家門繁栄する。これは人に真心をかけるからである。<br>
			　<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は、「仁は、百災を除く」ということをご存知なかったのではなかろうか。</p>
			<p></p>

			<h4 class="c-heading__s">「敵に塩を送る」、その仁あればこそ、お家は繁栄す</h4>
			<p>　<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は、戦の道は<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公より劣っていた。しかし、悪と呼ばれたことはない。<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公には仁を保つ心がある。このごろ<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>公は一門である駿河の今川、小田原の北条らとも不和になった。両国の大将は武田を憎み、甲州へ塩留めをした。民は塩に難儀していた。これを聞かれた<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は、塩を甲州へ送ろうといわれた。家臣一同は、「武田との干戈（かんか）の最中に塩を送れば、所詮武田にかなわずと、追従賄賂と取り沙汰されること無念」と強く反対した。</p>
			<p>　これに対し、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は、「その方達は、ただ<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>一人を恨んでいうことだ。我はそうではない。甲斐の国の民を不便（ふびん）と思うからである。民を救うは大将の法務。塩を送ったとて、戦場の障りになるまい」と三百駄の塩を送られた※。眼前の敵国へ塩を送られたのは、仁の勝る点である。陰徳あれば陽報あり（『人知れず善行を行えば、必ず善い報いがある』の意）、貫禄ともに不足なく、御家はご繁栄している。</p>
			<p>※故事成語の「敵に塩」は、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>の甲斐に塩を送った古事に由来する。相手を有利にさせることの意。</p>

			<h4 class="c-heading__s">「信玄許すまじき！」と謙信公、川中島へ、いざ出陣！</h4>
			<p>　こうして<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は都より帰国された。ところが、留守の間、武田軍によって大田切が放火され、割ヶ嶽城を攻め落とされたことを聞かされた。「<a href="https://kawanakajima.nagano.jp/character/takeda-shingen">信玄</a>め、理由もなく約束に背き、無法の働き、まさに言語同断である！」。大いに立腹された<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">謙信</a>公は、今度こそ有無の決戦をせんと春日山を出馬された。随う人々は、</p>
