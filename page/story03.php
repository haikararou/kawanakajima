		<div class="c-section__column2__w65">
			<h4 class="c-heading__s">義清、謙信公を頼って合力を懇願す</h4>
			<p>　武田に信濃を追われ千辛万苦してようやく越後に落ち延びた<a href="https://kawanakajima.nagano.jp/character/category/other/#murakami-yoshikiyo">村上左衛門尉義清</a>。勇気も絶え、両頬も自然と寒く感じる秋風である。</p>
            <p>　<a href="https://kawanakajima.nagano.jp/character/category/other/#murakami-yoshikiyo">義清</a>の先妻は、加治安芸守の娘で、<a href="https://kawanakajima.nagano.jp/character/category/main/#uesugi-kenshin">謙信</a>公の姪※1　である。こうした縁もあり、子の源吾国清は<a href="https://kawanakajima.nagano.jp/character/category/main/#uesugi-kenshin">謙信</a>公に貰われ、春日山城に在城。<a href="https://kawanakajima.nagano.jp/character/category/other/#murakami-yoshikiyo">義清</a>は国清に、<a href="https://kawanakajima.nagano.jp/character/category/main/#uesugi-kenshin">謙信</a>公にまみえることを頼んだ。</p>
			<p>　<a href="https://kawanakajima.nagano.jp/character/category/main/#uesugi-kenshin">謙信</a>公と対面した<a href="https://kawanakajima.nagano.jp/character/category/other/#murakami-yoshikiyo">義清</a>は「このたび、武田信玄のために上田原の合戦に打ち負け、我が葛尾城までも放火され、誠に無念。この上は<a href="https://kawanakajima.nagano.jp/character/category/main/#uesugi-kenshin">謙信</a>公のお救いを蒙り、会稽の恥※2　をそそぎたく存ずる。その代わり、高井郡笠原の庄、木島を献上するので、ひとえに帰国の合力を願い申す」と懇願した。しかし、<a href="https://kawanakajima.nagano.jp/character/category/other/#murakami-yoshikiyo">義清</a>の言葉は<a href="https://kawanakajima.nagano.jp/character/category/main/#uesugi-kenshin">謙信</a>公の心を動かさなかった。</p>
			<p> ※１　史料・伝承はなし<br>
            ※２　会稽の恥をそそぐ…中国の春秋時代、越王が呉王と戦って敗れたが、多年辛苦－臥薪嘗胆の後に、その恥をすすいだ古事。</p>

			<h4 class="c-heading__s">謙信公、ただ武士たるものとして義清の願いを受ける</h4>
			<p>　<a href="https://kawanakajima.nagano.jp/character/category/other/#murakami-yoshikiyo">義清</a>の願いに、<a href="https://kawanakajima.nagano.jp/character/category/main/#uesugi-kenshin">謙信</a>公は「武田がために国を追い出されたのは、貴所の武略が足らぬ結果。他の力に頼らず、貴殿の才覚で帰国すべきであろう」と答える。短慮の義清はむきになって、「隣国のよしみでご加勢下さるは武士の習い。いかなる思し召しで合力いただけないのか」と詰め寄った。</p>
            <p>　そこで、<a href="https://kawanakajima.nagano.jp/character/category/main/#uesugi-kenshin">謙信</a>公は「笠原木島の領地など馬の飼料にも足らない。欲のために味方致すは、弓箭（きゅうせん）※の穢（けが）れとなり、拙者にはできない。ただ一途の頼みということであれば、合力いたそう」と言われた。<a href="https://kawanakajima.nagano.jp/character/category/other/#murakami-yoshikiyo">義清</a>は後悔して恥ずかしく、しばらくは言葉も出なかった。「馬は疲れて毛長く、人は貧にして智短しの譬（たと）え、粗忽の一言をお許しいただき、ぜひお救い願い奉る」と頭を垂れて頼んだ。</p>
