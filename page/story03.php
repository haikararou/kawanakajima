            <p>　この子の背中には鱗のあざがあり、人々は蛇太郎と呼んだ。幼いころから力が強く、勇猛百人に秀で、後に駿河守といった。一説には野尻の池の主といわれる。弁才天は頭に蛇を載せておられるが、これを宇賀の神という。宇佐美は宇佐神とも記す。定行の背中には鱗のあざがあるので、宇賀神（うがじん）の化身であるのかもしれない。</p>

