			<p>　川中島合戦が終わって後に、<a href="https://kawanakajima.nagano.jp/character/category/main/#uesugi-kenshin">謙信</a>の姉婿・長尾越前守政景に謀反の企てがあった。信州野尻城主の宇佐美駿河守は、「我にお任せくだされ」と密かに<a href="https://kawanakajima.nagano.jp/character/category/main/#uesugi-kenshin">謙信</a>公に告げ、永禄七年（1564）七月五日、越前守を呼び寄せ、「弁天の湖水で漁をして慰め申そう」と小舟に乗せた。そして遥か樅ヶ崎の沖合に連れ出し、政景に向かって「貴殿に陰謀の企てがあることを我は知っている。我は元来この野尻の池の主、今日は貴殿を伴って湖に帰ろうと思う」といいながら、驚き立ち上がる越前守を抱き挟んで、湖水に飛び込み自害した。</p>
            <p>　越前守四十一歳、定行五十六歳であった。野尻には宇佐神の古城が残る。越前守の石碑は野尻真光寺にある。昔、源頼光の臣、坂田金時も出生は山、帰るも山。これも大将の武徳に愛でて、このような勇士が現れるものである。</p>
