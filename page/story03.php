		<div class="c-section__column2__w65">
			<h4 class="c-heading__s">生後二年間、左手を開かなかった晴信公</h4>
			<p>　<a href="https://kawanakajima.nagano.jp/character/category/main/#takeda-shingen">武田晴信</a>公がお生まれになったのは大永六年（1526）※１。ご幼名は太郎殿。生まれて二年の間、どうしたわけか、左の手をお開きにならなかった。父の<a href="https://kawanakajima.nagano.jp/character/category/takeda/#takeda-nobutora">信虎</a>も気を痛めていたが、恵林寺の快川和尚［かいせんおしょう］※1が<a href="https://kawanakajima.nagano.jp/character/category/main/#takeda-shingen">晴信</a>公のご相をご覧になったところ、「行く末めでたく智・仁兼備の相がある。昔、かの聖徳太子もご誕生から二年間は手が開かなかったが、翌年には手を開き、手には釈尊入滅後に国王の后が肌身離さずにいたゆかりの仏舎利を手にして生まれた。こうした先例もあるので、手が開かないのもきっと名将の印。心配することはないだろう」といった。</p>
            <p>　その予言どおり、<a href="https://kawanakajima.nagano.jp/character/category/main/#takeda-shingen">晴信</a>公の左手は開き、<a href="https://kawanakajima.nagano.jp/character/category/takeda/#takeda-nobutora">信虎</a>もたいそう喜んだが、このとき手には金の龍の目貫（めぬき）※2を持っていた。</p>
            <p>※1　快川和尚……快川紹喜（かいせんしょうき）。京都妙心寺派の禅僧。美濃の出身で<a href="https://kawanakajima.nagano.jp/character/category/main/#takeda-shingen">武田信玄</a>に請われ、恵林寺に入山。武田氏滅亡のとき、織田信長に反抗し、焼身成仏した。「心頭滅却すれば、火もおのずから涼し」と唱えたという。<br>
            ※2　甲越信戦録ではこのように<a href="https://kawanakajima.nagano.jp/character/category/main/#takeda-shingen">晴信</a>の生年が記されているが、当サイトの人物紹介では他の史料を総合した上で大永元年（1521）生としている。<br>
            ※3　目貫……刀を止める目釘の表面を飾る対の金具</p>
			
            <h4 class="c-heading__s">禅僧、富士の原で曽我十郎佑成の霊に出会う</h4>
            <p>　その後のこと、遊行中の禅僧が富士の裾野で夜を明かそうと座禅を組んでいると、芒（すすき）原から壮年の男がやって来て、「夜露に濡れるは痛ましかろう」と自分の庵に案内してくれた。</p>
            <p>　庵に入り、世間話をしているうち、禅僧が「どこの国かで鬨（とき）の声があがり、四方に煙火燃え上がるような音が聞こえる」という。これを聞いた男は顔色が青ざめ「はや迎えが参った」と出ていくが、その後も禅僧の耳には叫び声がひびくのみ。さては修羅の巷に苦しんでいる者たちに違いないと、禅僧は涙を流して読経すると、物音も静まり、例の男も立ち帰ってきた。「この有様は修羅の戦い、いかなる罪業でこうした苦しみを受けているのか」と禅僧は男に問いかける。すると男は「拙者こそ、曽我の十郎祐成が苦しむ姿でござる」という。</p>         
            <p>　建永二年（1207）五月、この場所で親の仇、工藤祐経を討ち果たした曽我兄弟。「弟の五郎（時宗）殿も同じく苦しんでおるのか」と禅僧がたずねると、男は「弟は母の言葉に従って、幼いころより読誦していた法華経の功力で再びこの世に生まれ、今は甲斐の国の太守と仰がれている。弟が生まれた時に手にしていたこの金の龍の目貫が、その印である」と、取り出した目貫を禅僧に渡した。そして「この苦しみから助けてくだされ」といって、姿を消した。<br>
            　禅僧は奇異の思いがして近くの福善寺に曽我兄弟の墓（石碑）を建て、二人の霊を供養した。</p>

            <h4 class="c-heading__s">晴信公は、曽我五郎の生まれ変わり</h4>
			<p>　甲州に来た禅僧は富士の原での話を恵林寺の快川和尚に語った。和尚は見覚えのある金の目貫をもらい受け、直ちに御殿の<a href="https://kawanakajima.nagano.jp/character/category/main/#takeda-shingen">晴信</a>公にことごとく申し上げ、金の目貫を差し上げた。これには<a href="https://kawanakajima.nagano.jp/character/category/main/#takeda-shingen">晴信</a>公も驚かれ、手にした目貫は一対となった。こういう次第で、<a href="https://kawanakajima.nagano.jp/character/category/main/#takeda-shingen">武田晴信</a>公を曽我五郎の生まれ替わりと申しあげる。<a href="https://kawanakajima.nagano.jp/character/category/main/#takeda-shingen">晴信</a>公は曽我一門の菩提のため、一寺を建立。今の万年山大仙寺※である。 ※大仙寺……<a href="https://kawanakajima.nagano.jp/character/category/takeda/#takeda-nobutora">信虎</a>・<a href="https://kawanakajima.nagano.jp/character/category/main/#takeda-shingen">信玄</a>・<a href="https://kawanakajima.nagano.jp/character/category/takeda/#takeda-katsuyori">勝頼</a>三代の墓がある武田氏の菩提寺「大泉寺」（山梨県甲府市古府中町）と考えられる。</p>

            <h4 class="c-heading__s">晴信公、出家して信玄と称する</h4>
			<p>　その後、<a href="https://kawanakajima.nagano.jp/character/category/main/#takeda-shingen">晴信</a>公は急に思い立って出家された。そのわけをご一門やご家臣方々がたずねたところ、<br>
            　第一は、父を駿河に追い出した不孝の罪をのがれるところがなく、天を恐れて俗体を捨てた。<br>
            　第二は、遠い都に上洛もできず位階昇進がないので、出家して僧正官に進むため。<br>
            　第三は、無理な成敗で領民を数多く殺した父の罪を助けるため。<br>
            　第四は、我が代わりとなり討ち死にした忠臣らのおかげで危難を逃れられた、その報恩のため。<br>
            　第五は、十六歳以来、討ち取った敵数七万余人、首数三万九千余級、その菩提のためである。 と仰せられた。<br>
            　一同はありがたいお気持ちに感じ入り、家臣の中より「我もお供を」と直ちに出家した者がいた。それは、<a href="https://kawanakajima.nagano.jp/character/category/main/#yamamoto-kansuke">山本勘助</a>、入道して道鬼、<a href="https://kawanakajima.nagano.jp/character/category/co-takeda/#obata-toramori">小幡山城守</a>、入道して日意といい、後に<a href="https://kawanakajima.nagano.jp/character/category/co-takeda/#sanada-yukitaka">真田</a>と<a href="https://kawanakajima.nagano.jp/character/category/co-takeda/#hara-toratane">原美濃守</a>も出家した。<br>
            　信玄公は、希望どおり勅許されて、法性院大僧正信玄と称した。</p>
