            <p>　宇佐美駿河守定行は、大職冠藤原鎌足公の子、内大臣淡海公（不比等）の末葉。越後第一の勇士で軍師。川中島合戦で武功比類なく、<a href="https://kawanakajima.nagano.jp/character/category/takeda/#takeda-nobushige">武田典厩信繁</a>を討ち取る。</p>
            <p>　甘粕近江守数直は、越後上田長坂の生まれ。武術勝れ智謀たくましく、勇功の陣将である。川中島合戦に武功ある。</p>
            <p>　柿崎和泉守景家は、大和源氏宇野七郎親治の末葉。米山城主、のちに琵琶島の主となり、智勇の陣将である。川中島合戦に<a href="https://kawanakajima.nagano.jp/character/category/main/#yamamoto-kansuke">山本勘助</a>を討ち取る。後に<a href="https://kawanakajima.nagano.jp/character/category/main/#uesugi-kenshin">謙信</a>と不和になり誅せらる。</p>
