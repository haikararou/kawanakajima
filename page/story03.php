			<p>　日本四大将の内、<a href="https://kawanakajima.nagano.jp/character/uesugi-kenshin">上杉謙信</a>公の父は、越後国頸城（くびき）郡府内の城主、長尾信濃守平為景の八男。武勇激しく隣国を脅し猛威を振い、信州も水内郡まで切り随えた。</p>
            <p>　<a href="https://kawanakajima.nagano.jp/character/category/main/#uesugi-kenshin">謙信公</a>は、享禄三年（1530）誕生。幼名は虎千代丸。はじめ長尾平蔵、後に上杉輝虎と名乗り、十八歳の時、入道して自ら不識庵謙信と称した。肉も食べず、妻妾はいうに及ばず、一人の侍女もそばにおくのを禁じ、精進持戒して、はなはだ廉直の人ゆえ、その徳に至った。終に<a href="https://kawanakajima.nagano.jp/character/category/uesugi/#uesugi-norimasa">上杉憲政</a>の譲りを請けて、関東管領職に進んだ。領内の佐渡に黄金を産出し、御本城は越後頸城郡春日山、軍卒五万余人、侍大将三十二将の総大将である。</p>

			<h4 class="c-heading__s">直江、宇佐美、甘粕、柿崎、これぞ上杉四天王</h4>
